package Lec_2;

public class Operators {
    public static void main(String[] args) {
        //ARITHMETIC
        System.out.println(7/2);
        //COMPARISON
        System.out.println(7>=2);
        //LOGICAl
        System.out.println((7<2)&&(7>=2));
        System.out.println((7<2)||(7>=2));
    }
}
