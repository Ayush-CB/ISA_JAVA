package Lec_2;

public class Data_types {
    public static void main(String[] args) {
        //BYTE
        byte b=100;
        //BOOLEAN
        boolean c=true;
        //INTEGER
        int d=500;
        //SHORT
        short e=5000;
        //LONG
        long f=1000000;
        //CHARACTER
        char g='h';
        //DOUBLE
        double i=9.80;
        //FLOAT
        float l=7.99f;
        System.out.println(7.0/3);
    }
}
